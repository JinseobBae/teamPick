    public String getTeamLevel(String teamName){
        String teamLevel = "";
        if(sTeamList.contains(teamName))
            teamLevel = "S";
        else if(aTeamList.contains(teamName))
            teamLevel = "A";
        else if(bTeamList.contains(teamName))
            teamLevel = "B";
        else if(cTeamList.contains(teamName))
            teamLevel = "C";
        else if(dTeamList.contains(teamName))
            teamLevel = "D";
        else if(zTeamList.contains(teamName))
            teamLevel = "Z";
        else
            teamLevel = "NONE";

        return teamLevel;
    }


    public List<Team> convertToTeam(){
     List<Team> resultList = new ArrayList<>();

     initTeamLevelList();
     long cnt = 1;
     for(String team : teamList){
         resultList.add(new Team(
                 cnt, team, getTeamLevel(team), getRate(getTeamLevel(team))
         ));
          cnt++;
     }
     return resultList;
    }

    private int getRate(String teamLevel){
        int rate = 0;
        switch (teamLevel){
            case "Z" : rate = 5;
                break;
            case "S" : rate = 5;
                break;
            case "A" : rate = 4;
                break;
            case "B" : rate = 3;
                break;
            case "C" : rate = 2;
                break;
            case "D" : rate = 1;
                break;
        }
        return rate;
    }
